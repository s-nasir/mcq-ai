import csv

def escape_text(text):
    # Escape double quotes by doubling them
    return text.replace('"', '""')

def create_csv(data, filename='prompt_data.csv'):
    with open(filename, 'w', newline='', encoding='utf-8') as csvfile:
        fieldnames = ['input', 'output']
        writer = csv.DictWriter(csvfile, fieldnames=fieldnames)

        writer.writeheader()

        for entry in data:
            # Prepare the input text
            question_text = f"Question: \"{escape_text(entry['Question'])}\"\nOptions:"
            for option_key, option_value in entry['Options'].items():
                question_text += f"\n{option_key}: \"{escape_text(option_value)}\""

            # Prepare the output text
            answer_text = f"Correct Answer: {entry['CorrectAnswer']}\nOption Analysis:"
            for option_key, option_value in entry['OptionAnalysis'].items():
                answer_text += f"\n{option_key}: \"{escape_text(option_value)}\""

            # Write to CSV
            writer.writerow({
                'input': question_text,
                'output': answer_text
            })

# Use the function
create_csv(data)

